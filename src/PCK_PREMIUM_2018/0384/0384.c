int main()
{
	int a, n, m;
	scanf("%d %d %d", &a, &n, &m);
	int y = 1, counter = 0, sumNum;
	long long x;
	while (1) {
		x = pow(y + a, n);
		if (x > m) {
			break;
		}
		sumNum = outputDigitsSum(x);
		if (y == sumNum) {
			counter++;
		}
		y++;
	}
	printf("%d\n", counter);
    return 0;
}

int outputDigitsSum(int num) {
	int rtnSum = 0;
	while (num > 0) {
		rtnSum += (num % 10);
		num /= 10;
	}
	return rtnSum;
}
