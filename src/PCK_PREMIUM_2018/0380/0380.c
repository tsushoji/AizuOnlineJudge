int main()
{
    int input;
    int output;
    scanf("%d\n", &input);
    if(!canValidateCheck(input))
    {
        return(0);
    }
    output = (input - 30) / 2;
    printf("%d\n",output);
    return 0;
}

bool canValidateCheck(int tarNum)
{
    if(30 > tarNum || tarNum > 100 || !(tarNum % 2 == 0))
    {
        return false;
    }
    return true;
}
