int main()
{
    int N, C, i, num, output;
    int sum = 0;
    scanf("%d %d", &N, &C);
    for(i = 0; i < C; i++){
        scanf("%d ", &num);
        sum += num;
    }
    output = sum / (N + 1);
    if(sum % (N + 1)){
       output += 1;
    }
    printf("%d\n", output);
    return 0;
}
